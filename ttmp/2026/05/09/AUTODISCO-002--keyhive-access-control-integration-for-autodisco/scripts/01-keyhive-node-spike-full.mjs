import * as KH from '/home/manuel/code/wesen/2026-05-09--automerge-discord/node_modules/@keyhive/keyhive/pkg-node/keyhive_wasm.js';
KH.setPanicHook();
const a = await KH.Keyhive.init(KH.Signer.generateMemory(), KH.CiphertextStore.newInMemory(), (...args)=>console.log('event', args.length));
const b = await KH.Keyhive.init(KH.Signer.generateMemory(), KH.CiphertextStore.newInMemory(), ()=>{});
console.log('ids', a.idString, b.idString);
const card = await b.contactCard();
console.log('card', card.toJson().slice(0,80));
const individual = await a.receiveContactCard(KH.ContactCard.fromJson(card.toJson()));
console.log('recv', individual.individualId.bytes.length);
const group = await a.generateGroup([]);
console.log('group', group.groupId.toString(), group.id.toBytes().length);
const zero = new KH.ChangeId(new Uint8Array(32));
const doc = await a.generateDocument([group.toPeer()], zero, []);
console.log('doc', doc.doc_id.toString());
for (const s of ['pull','read','comment','edit','admin']) console.log(s, KH.Access.tryFromString(s)?.toString());
const access = KH.Access.tryFromString('read');
const del = await a.addMember(individual.toAgent(), doc.toMembered(), access, []);
console.log('delegation verify', del.verify());
const enc = await a.tryEncrypt(doc, new KH.ChangeId(new Uint8Array(32).fill(1)), [], new TextEncoder().encode('hello'));
const encrypted = enc.encrypted_content();
const plain = await a.tryDecrypt(doc, encrypted);
console.log('decrypt self', new TextDecoder().decode(plain));
const rev = await a.revokeMember(individual.toAgent(), true, doc.toMembered());
console.log('revocations', rev.length);
